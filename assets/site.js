let listings = embeddedSampleListings;

let currentLang = localStorage.getItem("properlyLang") || "sk";

function t(key) {
  return translations[currentLang][key] || translations.sk[key] || key;
}

function listingTitle(listing) {
  return `${listing.title[currentLang] || listing.title.sk}, ${listing.location.replace("Bratislava - ", "")}`;
}

function typeLabel(type) {
  const map = {
    apartment: t("typeApartment"),
    house: t("typeHouse"),
    commercial: t("typeCommercial")
  };
  return map[type] || type;
}

function normalizeListingRecord(record) {
  const title = typeof record.title === "string" ? { sk: record.title, en: record.title, tr: record.title, ru: record.title } : record.title;
  return {
    id: record.id,
    mode: record.mode,
    type: record.type,
    location: record.location,
    image: record.image || "assets/images/listing-apartment-stare-mesto.png",
    priceText: record.priceText || "Cena na vyžiadanie",
    priceValue: Number(record.priceValue || 0),
    area: Number(record.area || 0),
    rooms: record.rooms || "-",
    title,
    description: record.description || sampleDescriptions[record.type] || sampleDescriptions.apartment
  };
}

async function loadListingsFromBackend() {
  const source = document.querySelector("meta[name='properly-listings-source']")?.content;
  if (!source) return;

  const response = await fetch(source);
  if (!response.ok) throw new Error(`Unable to load listings from ${source}`);
  const payload = await response.json();
  const records = Array.isArray(payload) ? payload : payload.listings;
  if (!Array.isArray(records)) throw new Error("Listing source must be an array or { listings: [] }.");
  listings = records.map(normalizeListingRecord);
}

function applyTranslations() {
  document.documentElement.lang = currentLang;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[currentLang][key]) {
      element.textContent = translations[currentLang][key];
    }
  });
  document.querySelectorAll("[data-lang]").forEach((button) => {
    const active = button.dataset.lang === currentLang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });
  renderDynamicAreas();
}

function locationIcon() {
  return `<svg viewBox="0 0 24 24" aria-hidden="true"><path d="M12 21s7-5.3 7-11a7 7 0 1 0-14 0c0 5.7 7 11 7 11Z" fill="none" stroke="currentColor" stroke-width="2"/><circle cx="12" cy="10" r="2.5" fill="currentColor"/></svg>`;
}


function bindLanguageSwitcher() {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      currentLang = button.dataset.lang;
      localStorage.setItem("properlyLang", currentLang);
      applyTranslations();
    });
  });
}

function bindFeaturedFilters() {
  document.querySelectorAll("[data-feature-filter]").forEach((button) => {
    button.addEventListener("click", () => {
      document.querySelectorAll("[data-feature-filter]").forEach((item) => item.classList.remove("is-active"));
      button.classList.add("is-active");
      renderFeatured();
    });
  });
}

function bindListingFilters() {
  const form = document.getElementById("listingFilters");
  if (!form) return;
  form.addEventListener("input", renderListingsGrid);
}

function bindHeroSearch() {
  const form = document.getElementById("homeSearch");
  if (!form) return;
  let mode = "buy";
  form.querySelectorAll("[data-search-mode]").forEach((button) => {
    button.addEventListener("click", () => {
      mode = button.dataset.searchMode;
      form.querySelectorAll("[data-search-mode]").forEach((item) => item.classList.remove("is-active"));
      button.classList.add("is-active");
    });
  });
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const params = new URLSearchParams(new FormData(form));
    params.set("mode", mode);
    window.location.href = `listings.html?${params.toString()}`;
  });
}

function hydrateListingFiltersFromUrl() {
  const form = document.getElementById("listingFilters");
  if (!form) return;
  const params = new URLSearchParams(window.location.search);
  params.forEach((value, key) => {
    if (form.elements[key]) form.elements[key].value = value;
  });
}

function bindForms() {
  document.querySelectorAll("[data-contact-form]").forEach((form) => {
    if (form.dataset.bound === "true") return;
    form.dataset.bound = "true";
    form.addEventListener("submit", (event) => {
      event.preventDefault();
      const status = form.querySelector(".form-status");
      if (status) status.textContent = t("formSent");
      form.reset();
    });
  });
}

async function init() {
  bindLanguageSwitcher();
  bindFeaturedFilters();
  bindHeroSearch();
  bindListingFilters();
  hydrateListingFiltersFromUrl();
  bindForms();
  try {
    await loadListingsFromBackend();
  } catch (error) {
    const count = document.getElementById("listingCount");
    if (count) count.textContent = "Listing source unavailable; showing embedded sample data.";
  }
  applyTranslations();
}

document.addEventListener("DOMContentLoaded", init);
